                    <article class="particular-committee-description-article clearfix">
                        <h3 class="particular-committee-description-h"> Energy4Me </h3>
                        <div class="particular-committee-description-img float">
                            <img src="../img/kd2.jpg" alt="Energy4Me">
                        </div>
                        <div class="particular-committee-description-float float">
                            <p class="particular-committee-description-p">
                                International Education Program SPE – Energy4me is an event, that provides technical information in energetic, science and technologies, which can be understandable for schools students. The project also makes free access to oil &amp; gas knowledge in different languages. Activists of Gubkin University SPE Student Chapter visit schools and hold master classes for pupils with using visual materials and teaching them what oil is, how it is extracted , how do we drill a well etc.
                            </p>
                        </div>
                    </article>
                    <article class="particular-committee-description-article clearfix">
                        <h3 class="particular-committee-description-h"> Petroolympic Games </h3>
                        <div class="particular-committee-description-img float">
                            <img src="../img/kd3.jpg" alt="Petroolympic Games">
                        </div>
                        <div class="particular-committee-description-float float">
                            <p class="particular-committee-description-p">
                                An intellectual quiz “PetroOlympic Games” is focused on the theoretical knowledge in the oil and gas industry. It is a question and answer game between five teams. The questions are usually based on such disciplines as Geosciences, Chemical Engineering, Drilling, Mechanical Engineering, Reservoir Engineering, Offshore Petroleum Engineering, Oil and Gas Transportation and Storage. Points are summarized in accordance with the correctly answered questions. There are three rounds in the game. The members of each team usually get an extensive experience working in a team.
                            </p>
