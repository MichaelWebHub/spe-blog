            <h1 class="faq-title"> FAQ </h1>
            <div class="question-answer">
                <h3 class="question">
                    What is SPE?
                </h3>
                <p class="answer">
                    SPE is the largest individual member organization serving managers, engineers, scientists and other professionals worldwide in the upstream segment of the oil and gas industry.
                </p>
            </div>
            <div class="question-answer">
                <h3 class="question">
                    What is SPE’s mission?
                </h3>
                <p class="answer">
                    To collect, disseminate, and exchange technical knowledge concerning the exploration, development and production of oil and gas resources, and related technologies for the public benefit; and to provide opportunities for professionals to enhance their technical and professional competence.
                </p>
            </div>
            <div class="question-answer">
                <h3 class="question">
                    What is SPE’s vision?
                </h3>
                <p class="answer">
                    Enable the global oil and gas E&amp;P industry to share technical knowledge needed to meet the world’s energy needs in a safe and environmentally responsible manner.
                </p>
            </div>
            <div class="question-answer">
                <h3 class="question">
                    Why is it a great opportunity for students?
                </h3>
                <p class="answer">
                    With more than 230 student chapters around the world, SPE makes it easy for them to network with industry professionals and possible employers, benefit from technology discussions at SPE meetings and events, and receive society-sponsored scholarships.
                </p>
            </div>
            <div class="question-answer">
                <h3 class="question">
                    Why should I join SPE as a student?
                </h3>
                <div class="answer">
                    As an SPE student member, you will have many benefits:
                    <ul>
                        <li> Meet practicing professionals at meetings or society events </li>
                        <li> Apply for a mentor and receive practical career advice </li>
                        <li> Receive discounts on textbooks, reference books, and other Society publications </li>
                        <li> Apply for a number of SPE scholarships, with a quarter of a million awarded annually </li>
                        <li> Participate in various SPE programs: eMentoring, online communities and paper contests </li>
                        <li> Have your dues paid by Chevron, corporate sponsor for SPE student dues program </li>
                    </ul>
                    … and many more!
                </div>
            </div>
